package bean;

public class BloggerType {
	private int id;
	private String name;
		
	
	public BloggerType() {
		super();
	}
	public BloggerType(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
